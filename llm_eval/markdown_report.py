"""Markdown report format for GitHub PR comments and documentation."""

from __future__ import annotations

from typing import Any

from llm_eval.models import EvalResult


def format_markdown_report(
    results: list[EvalResult],
    summary: dict[str, Any],
    metadata: dict[str, Any] | None = None,
) -> str:
    """Format evaluation results as Markdown.

    Suitable for GitHub PR comments, issues, and documentation.

    Args:
        results: List of evaluation results.
        summary: Summary statistics dictionary.
        metadata: Optional report metadata.

    Returns:
        Markdown-formatted report string.
    """
    threshold = summary.get("threshold", 0.7)
    metric_scores = summary.get("metric_scores", {})
    overall = summary.get("overall_score", 0.0)
    total = summary.get("total_samples", 0)
    pass_count = summary.get("pass_count", 0)
    fail_count = summary.get("fail_count", 0)
    pass_rate = summary.get("pass_rate", 0.0)
    overall_status = "✅ PASS" if overall >= threshold else "❌ FAIL"

    lines: list[str] = []
    lines.append("# 🧪 llm-eval — Evaluation Report")
    lines.append("")

    # Metadata
    if metadata:
        parts: list[str] = []
        for key in ("timestamp", "version", "config_path", "git_hash"):
            if key in metadata:
                label = key.replace("_", " ").title()
                parts.append(f"**{label}:** `{metadata[key]}`")
        if parts:
            lines.append(" · ".join(parts))
            lines.append("")

    # Summary cards
    lines.append("## Summary")
    lines.append("")
    lines.append("| Metric | Value |")
    lines.append("|--------|-------|")
    lines.append(f"| **Overall Score** | **{overall:.4f}** {overall_status} |")
    lines.append(f"| **Threshold** | {threshold:.2f} |")
    lines.append(f"| **Samples** | {total} |")
    lines.append(f"| **Passed** | {pass_count} ✅ |")
    lines.append(f"| **Failed** | {fail_count} ❌ |")
    lines.append(f"| **Pass Rate** | {pass_rate:.1%} |")
    lines.append("")

    # Metric details
    lines.append("## 📊 Metric Scores")
    lines.append("")
    lines.append("| Metric | Mean | Min | Max | Status |")
    lines.append("|--------|------|-----|-----|--------|")
    for metric_name, scores in metric_scores.items():
        mean = scores["mean"]
        status = "✅" if mean >= threshold else "❌"
        lines.append(
            f"| {metric_name} | {mean:.4f} | {scores['min']:.4f} | "
            f"{scores['max']:.4f} | {status} |"
        )
    lines.append("")

    # Per-sample table (only if <= 20 samples to keep PR comments readable)
    if results and len(results) <= 20:
        lines.append("## 📋 Per-Sample Results")
        lines.append("")
        metric_names = list(metric_scores.keys())
        header = "| Sample | " + " | ".join(metric_names) + " | Overall |"
        sep = "|--------|" + "|".join(["------"] * len(metric_names)) + "|---------|"
        lines.append(header)
        lines.append(sep)
        for r in results:
            cells = [f"#{r.sample_index}"]
            for m in r.metrics:
                icon = "✅" if m.score >= threshold else "❌"
                cells.append(f"{m.score:.2f} {icon}")
            cells.append(f"**{r.overall_score:.2f}**")
            lines.append("| " + " | ".join(cells) + " |")
        lines.append("")
    elif results:
        lines.append(f"*(Per-sample table omitted — {len(results)} samples)*")
        lines.append("")

    # Footer
    lines.append("---")
    lines.append("*Generated by llm-eval*")
    return "\n".join(lines)


