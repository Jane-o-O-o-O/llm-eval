"""Tests for the Markdown report format (llm_eval.markdown_report)."""

from __future__ import annotations

import pytest

from llm_eval.markdown_report import format_markdown_report
from llm_eval.models import EvalResult, MetricResult


@pytest.fixture
def sample_results():
    return [
        EvalResult(
            sample_index=0,
            metrics=[
                MetricResult(name="faithfulness", score=0.9, details={"reasoning": "Good"}),
                MetricResult(name="answer_relevancy", score=0.8, details={}),
            ],
        ),
        EvalResult(
            sample_index=1,
            metrics=[
                MetricResult(name="faithfulness", score=0.7, details={}),
                MetricResult(name="answer_relevancy", score=0.85, details={}),
            ],
        ),
    ]


@pytest.fixture
def sample_summary():
    return {
        "total_samples": 2,
        "overall_score": 0.8125,
        "pass_count": 2,
        "fail_count": 0,
        "pass_rate": 1.0,
        "threshold": 0.7,
        "metric_scores": {
            "faithfulness": {"mean": 0.8, "min": 0.7, "max": 0.9},
            "answer_relevancy": {"mean": 0.825, "min": 0.8, "max": 0.85},
        },
    }


class TestMarkdownReport:
    """Tests for format_markdown_report."""

    def test_contains_title(self, sample_results, sample_summary):
        report = format_markdown_report(sample_results, sample_summary)
        assert "# 🧪 llm-eval" in report

    def test_contains_overall_score(self, sample_results, sample_summary):
        report = format_markdown_report(sample_results, sample_summary)
        assert "0.8125" in report

    def test_contains_threshold(self, sample_results, sample_summary):
        report = format_markdown_report(sample_results, sample_summary)
        assert "0.70" in report

    def test_contains_pass_status(self, sample_results, sample_summary):
        report = format_markdown_report(sample_results, sample_summary)
        assert "✅ PASS" in report

    def test_fail_status_when_below_threshold(self, sample_results):
        summary = {
            "total_samples": 1,
            "overall_score": 0.3,
            "pass_count": 0,
            "fail_count": 1,
            "pass_rate": 0.0,
            "threshold": 0.7,
            "metric_scores": {
                "faithfulness": {"mean": 0.3, "min": 0.3, "max": 0.3},
            },
        }
        report = format_markdown_report(sample_results, summary)
        assert "❌ FAIL" in report

    def test_contains_metric_table(self, sample_results, sample_summary):
        report = format_markdown_report(sample_results, sample_summary)
        assert "| Metric | Mean | Min | Max | Status |" in report
        assert "faithfulness" in report
        assert "answer_relevancy" in report

    def test_contains_per_sample_table_when_few_samples(self, sample_results, sample_summary):
        report = format_markdown_report(sample_results, sample_summary)
        assert "Per-Sample Results" in report
        assert "#0" in report
        assert "#1" in report

    def test_omits_per_sample_table_when_many_samples(self, sample_summary):
        many_results = [
            EvalResult(
                sample_index=i,
                metrics=[MetricResult(name="faithfulness", score=0.8)],
            )
            for i in range(25)
        ]
        report = format_markdown_report(many_results, sample_summary)
        assert "omitted" in report

    def test_with_metadata(self, sample_results, sample_summary):
        metadata = {
            "timestamp": "2026-05-14T00:00:00Z",
            "version": "0.7.0",
            "config_path": "evals.yaml",
        }
        report = format_markdown_report(sample_results, sample_summary, metadata)
        assert "2026-05-14" in report
        assert "0.7.0" in report

    def test_without_metadata(self, sample_results, sample_summary):
        report = format_markdown_report(sample_results, sample_summary)
        assert "llm-eval" in report

    def test_empty_results(self):
        summary = {
            "total_samples": 0,
            "overall_score": 0.0,
            "pass_count": 0,
            "fail_count": 0,
            "pass_rate": 0.0,
            "threshold": 0.7,
            "metric_scores": {},
        }
        report = format_markdown_report([], summary)
        assert "llm-eval" in report
        assert "0" in report

    def test_contains_footer(self, sample_results, sample_summary):
        report = format_markdown_report(sample_results, sample_summary)
        assert "Generated by llm-eval" in report

    def test_contains_sample_counts(self, sample_results, sample_summary):
        report = format_markdown_report(sample_results, sample_summary)
        assert "2" in report  # total samples
